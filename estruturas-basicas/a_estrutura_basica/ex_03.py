'''
Uma professora precisa calcular a média de um aluno que realizou duas provas.
Faça um algoritmo que leia duas notas, calcule e imprima a média aritmética do aluno.
'''

n1 = float(input('Digite a primeira nota:'))
n2 = float(input('Digite a segunda nota:'))
media = (n1+n2)/2
print('Média final:', media)